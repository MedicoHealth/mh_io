        return '<a href="'.$link.'" target="_blank" class="linked-url"><img src="images/tw.svg" alt=""></a>';
    }elseif (strpos($link, 'linkedin.') !== false){
        return '<a href="'.$link.'" target="_blank" class="linked-url"><img src="images/le.svg" alt=""></a>';
    }

    return '<a href="'.$link.'" target="_blank" class="linked-url"><img src="images/socialicons_web.svg" alt=""></a>';

}



